/**
 * Insert extended Bible content into database
 */
export async function insertExtendedContent(db: any): Promise<void> {
    const allVerses = [
        ...extendedBibleContent.john1,
        ...extendedBibleContent.john3,
        ...extendedBibleContent.john14,
        ...extendedBibleContent.matthew5,
        ...extendedBibleContent.matthew6,
        ...extendedBibleContent.romans8,
        ...extendedBibleContent.proverbs3,
        ...extendedBibleContent.corinthians13,
        ...extendedBibleContent.philippians4,
    ];

    for (const verse of allVerses) {
        await db.runAsync(
            `INSERT OR IGNORE INTO verses (book_id, chapter_number, verse_number, text, version_id)
       VALUES (?, ?, ?, ?, ?)`,
            verse.bookId,
            verse.chapterNumber,
            verse.verseNumber,
            verse.text,
            verse.versionId
        );
    }

    console.log(`✅ ${allVerses.length} extended Bible verses inserted`);
}
